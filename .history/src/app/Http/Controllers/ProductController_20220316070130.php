    public function index(Request $request)
    {
        $keyword = $request->input('keyword');
 
        $query = Product::query();
 
        if (!empty($keyword)) {
            $query->where('h_no', 'LIKE', "%{$keyword}%")
                ->orWhere('model', 'LIKE', "%{$keyword}%")
                ->orWhere('material', 'LIKE', "%{$keyword}%")
                ->orWhere('plate_thickness', 'LIKE', "%{$keyword}%");
        }
 
        $works = $query->get();
 
        return view('products.index', compact('works'));
        // return view('products.index');
    }

